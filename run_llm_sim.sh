#!/usr/bin/env bash
set -e

# ---------------------------------------------------------------------------
# Configurable defaults
# ---------------------------------------------------------------------------
CONFIG="configs/local_config.yaml"
CASE_FILE="./data/case_ACTIVSg200.m"
MAX_ITER=20

# ---------------------------------------------------------------------------
# Optional positional argument overrides
# ---------------------------------------------------------------------------
[ -n "$1" ] && CONFIG="$1"
[ -n "$2" ] && CASE_FILE="$2"
[ -n "$3" ] && MAX_ITER="$3"

# ---------------------------------------------------------------------------
# Interactive goal prompt
# ---------------------------------------------------------------------------
read -p "Enter simulation prompt: " PROMPT

# ---------------------------------------------------------------------------
# Summary header
# ---------------------------------------------------------------------------
echo ""
echo "============================================================"
echo "  LLM-Sim Run"
echo "============================================================"
echo "  Config:    $CONFIG"
echo "  Case file: $CASE_FILE"
echo "  Max iter:  $MAX_ITER"
echo "  Prompt:    $PROMPT"
echo "============================================================"
echo ""

# ---------------------------------------------------------------------------
# Run
# ---------------------------------------------------------------------------
python -m llm_sim --config "$CONFIG" "$CASE_FILE" "$PROMPT" --max-iter "$MAX_ITER"
